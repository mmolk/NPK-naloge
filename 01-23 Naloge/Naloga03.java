

public class Naloga03 {
	
	public static void main(String args[]) {
		/* MAIN */
		
		double premer = Double.parseDouble(args[0]);
		final double PI = 3.141592653589;
		double obseg = PI * premer;
		double ploscina = PI * ((premer/2) * (premer/2));
				
		System.out.println("\nObseg kroga je: " + obseg);
		System.out.println("Ploscina kroga je: " + ploscina + "\n");
		
		/* MAIN - konec */
		}
	
		/* METODE */
		
	
}